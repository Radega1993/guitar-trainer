import { readFileSync, readdirSync, writeFileSync } from 'fs';
import path from 'path';

const root = path.resolve('.');
const notesDir = path.join(root, 'assets/audio/notes');
const outFile = path.join(root, 'src/services/audio/noteAudioAssets.ts');

const noteFiles = readdirSync(notesDir)
  .filter((f) => f.endsWith('.mp3'))
  .sort();

const noteEntries = noteFiles
  .map((file) => {
    const key = file.replace(/\.mp3$/, '');
    return `  '${key}': require('../../../assets/audio/notes/${file}'),`;
  })
  .join('\n');

const content = `/** Generated by scripts/gen-note-audio-assets.mjs — do not edit manually. */
import { FeedbackSound } from './noteAudioCatalog';

export const NOTE_AUDIO_MODULES: Record<string, number> = {
${noteEntries}
};

export const FEEDBACK_AUDIO_MODULES: Record<FeedbackSound, number> = {
  correct: require('../../../assets/audio/feedback/correct.mp3'),
  wrong: require('../../../assets/audio/feedback/wrong.mp3'),
  tap: require('../../../assets/audio/feedback/tap.mp3'),
};

export function getNoteAudioModule(key: string): number | undefined {
  return NOTE_AUDIO_MODULES[key];
}

export function getFeedbackAudioModule(kind: FeedbackSound): number {
  return FEEDBACK_AUDIO_MODULES[kind];
}
`;

writeFileSync(outFile, content);
console.log(`Generated ${outFile} with ${noteFiles.length} note assets.`);
